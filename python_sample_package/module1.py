def cal_square(number: int) -> int:
    """Calculating Square

    Args:
        number : int

    Returns:
        square_val :int
    """
    return number**2


def doubler(number: int) -> int:
    """Double the Passing Values

    Args:
        number : int

    Returns:
        values_multiple_by_2 : int
    """
    return number*2
